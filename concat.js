// concat - Concatenates objects into one object
const firstName = "John"
const lastName = "Smith"
const age = 25
const hobbies = ["dancing", "swimming", "archery"]
const sentence = firstName.concat(lastName, age, hobbies)
console.log(sentence)

const numsA = [1, 2, 3]
const numsB = [4, 5, 6]
const numsC = [7, 8, 9]
const numbers = numsA.concat(numsB, numsC)
console.log(numbers)